<p>
<a target="_blank" href="https://loda-lang.org/">LODA</a> is an assembly language,
a computational model, and a distributed tool for mining programs.
You can use it to generate and search programs that compute integer sequences from the
<a target="_blank" href="https://oeis.org/">On-Line Encyclopedia of Integer Sequences®</a> (OEIS®).
The goal of the project is to find new formulas and
more efficient algorithms for a wide range of non-trivial integer sequences.
</p>

