<p>
LODA runs on Windows 10 or higher, Linux and macOS.
<b><i>Please install the <code>git</code> version control tool before running LODA:</i></b>
<ul>
<li>On Windows, please install <a target="_blank" href="https://gitforwindows.org/">Git for Windows</a>.</li>
<li>On Ubuntu/Debian, you can install it using <code>sudo apt install git</code>.</li>
<li>On macOS, it is included in <a href="https://brew.sh/">homebrew</a>.</li>
</ul>
